void opcontrol()
{
    while (true){
		if (game != 3) {drawArectangle(0,0,480,240,(Color){0,0,0,255});}
		if (!gameStarted && master.get_digital_new_press(pros::E_CONTROLLER_DIGITAL_LEFT) && game > 0){
			game--;
		} else if (!gameStarted && master.get_digital_new_press(pros::E_CONTROLLER_DIGITAL_RIGHT) && game < numofgames){
			game++;
		}
		if (master.get_digital_new_press(pros::E_CONTROLLER_DIGITAL_A) && game != 0){
			gameStarted = true;
		}
		if (!gameStarted){
			switch(game){
				case 3:
						master.print(0, 0, "Game: Chess                    ");
				case 2:
						master.print(0, 0, "Game: Flappy Bird                    ");
				case 1:
						master.print(0, 0, "Game: Pong                    ");
				case 0:
					master.print(0, 0, "No game selected");
			}
		}
		if (game == 1 && lastgame != 1){//pong
			paddle1 = {215, 10};
			paddle2 = {215, 220};
			ball = {240, 120};
			ballvel = {0, 0};
			fps = (1000 / (20));
		}
		if (game == 2 && lastgame != 2){//flappy bird
			jumped = false;
			flappyY = 240;
			vel = 0;
			gravity = 0;
			pipeheight1 = -240;
			pipeheight2 = -240;
			pipeX1 = 0;
			pipeX2 = -180;
			lastnotpressed = true;
			score = 0;
		}
		if (game == 3 && lastgame != 3){//flappy bird
			char board[8][8] = {
				{'R','P',0,0,0,0,'p','r'},
				{'N','P',0,0,0,0,'p','n'},
				{'B','P',0,0,0,0,'p','b'},
				{'Q','P',0,0,0,0,'p','q'},
				{'K','P',0,0,0,0,'p','k'},
				{'B','P',0,0,0,0,'p','b'},
				{'N','P',0,0,0,0,'p','n'},
				{'R','P',0,0,0,0,'p','r'},
			};
			drawArectangle(0, 0, 480, 240, (Color){150,150,150,255});
		}
		if (game == 1){//pong
			//start = GetTime() * 1000;
			start = pros::c::millis();
			ball.x += ballvel.x;
			ball.y += ballvel.y;
			paddle1.x += ((float)master.get_analog(pros::E_CONTROLLER_ANALOG_LEFT_X) / 127) * 16;
			if ((float)master.get_analog(pros::E_CONTROLLER_ANALOG_LEFT_X) != 0 && ballvel.x == 0){
				ballvel.x = 6;
				ballvel.y = 6;
				gameStarted = true;
			}
			paddle2.x += ((float)master.get_analog(pros::E_CONTROLLER_ANALOG_RIGHT_X) / 127) * 16;
			if ((float)master.get_analog(pros::E_CONTROLLER_ANALOG_RIGHT_X) != 0 && ballvel.x == 0){
				ballvel.x = 6;
				ballvel.y = 6;
				gameStarted = true;
			}

			paddle1.x = (paddle1.x < 0) ? 0 : (paddle1.x > 430 ? 430 : paddle1.x);
			paddle2.x = (paddle2.x < 0) ? 0 : (paddle2.x > 430 ? 430 : paddle2.x);
			if (Collision((Rectangle){ball.x, ball.y, 8, 8}, (Rectangle){paddle2.x, paddle2.y, 50, 10})){
				ballvel.y = -ballvel.y;
			}
			if (Collision((Rectangle){ball.x, ball.y, 8, 8}, (Rectangle){paddle1.x, paddle1.y, 50, 10})){
				ballvel.y = -ballvel.y;
			}
			if (Collision((Rectangle){ball.x, ball.y, 8, 8}, (Rectangle){-10, 0, 10, 240}) || Collision((Rectangle){ball.x, ball.y, 8, 8}, (Rectangle){480, 0, 10, 240})){
				ballvel.x = -ballvel.x;
			}

			if (Collision((Rectangle){ball.x, ball.y, 8, 8}, (Rectangle){0, -10, 480, 10}) || Collision((Rectangle){ball.x, ball.y, 8, 8}, (Rectangle){0, 240, 480, 10})){
				game = 0;
				gameStarted = false;
			}

			//BeginDrawing();
			//ClearBackground((Color){0,0,0,255});
			drawArectangle(0,0,480,240, (Color){0,0,0,255});
			drawArectangle(paddle1.x, paddle1.y, 50, 10, (Color){255,255,255,255});
			drawArectangle(paddle2.x, paddle2.y, 50, 10, (Color){255,255,255,255});
			drawAcircle(ball.x, ball.y, 8, (Color){255,255,255,255});
			//EndDrawing();
			//end = GetTime() * 1000;
			end = pros::c::millis();
			if (end - start < fps){
				//WaitTime((fps - (end - start))/1000);
				pros::c::delay(fps-(end-start));
			}
		} else if (game == 2){
			if (master.get_digital_new_press(pros::E_CONTROLLER_DIGITAL_UP) && lastnotpressed){
				jumped = true;
				gameStarted = true;
    			gravity = 2;
			} else {
				lastnotpressed = true;
			}
			start = pros::c::millis();
			flappyY += vel;
			vel += gravity;
			if (vel > 20){
				vel = 20;
			} else if (vel < -20){
				vel = -20;
			}
			if (jumped){
				vel = -10;
				jumped = false;
			}
			if (pipeX1 >= 240){
				pipeX1 = -115;
				pipeheight1 = rand()%(-220+300 + 1) - 300;
				score++;
			} else if (gameStarted) {
				pipeX1 += 7 * max((int)((float)score / 10), 1);
			}
			if (pipeX2 >= 240){
				pipeX2 = -113;
				pipeheight2 = rand()%(-220+300 + 1) - 300;
				score++;
				
			} else if (gameStarted){
				pipeX2 += 7 * max((int)((float)score / 10), 1);
			}

			pros::screen::set_pen(0x000000);
			pros::screen::fill_rect(0,0,480,240);


			pros::screen::set_pen(0xFCBA00);
			pros::screen::fill_rect(flappyY,160,flappyY+25,160+25);

			pros::screen::set_pen(0x00FF00);
			pros::screen::fill_rect(pipeheight1,pipeX1,pipeheight1+400,pipeX1+25);
			pros::screen::fill_rect(pipeheight1+560,pipeX1,pipeheight1+400+560,pipeX1+25);
			
			if (pipeX1 >= 140 && pipeX1 <= 165 && (flappyY <= pipeheight1 + 400 || flappyY + 25 >= pipeheight1 + 560)){
				game = 0;
				gameStarted = false;
			}

			pros::screen::fill_rect(pipeheight2,pipeX2,pipeheight2+400,pipeX2+25);
			pros::screen::fill_rect(pipeheight2 + 560,pipeX2,pipeheight2+560+400,pipeX2+25);
			if (pipeX2 >= 140 && pipeX2 <= 165 && (flappyY <= pipeheight2 + 400 || flappyY + 25 >= pipeheight2 + 560)){
				game = 0;
				gameStarted = false;
			}
			
			if (((pros::c::millis()) - start) < 50){
				pros::c::delay((50 - ((pros::c::millis()+20) - start)));
			}
			end = pros::c::millis();
			pros::c::delay(20);
		} else if (game == 3){
			//start = GetTime() * 1000;
			start = pros::millis();

			
			mousex += ((float)master.get_analog(pros::E_CONTROLLER_ANALOG_LEFT_X) / 127) * 5;
			mousey += ((float)master.get_analog(pros::E_CONTROLLER_ANALOG_LEFT_Y) / 127) * -5;


			int32_t ispressed = master.get_digital_new_press(pros::E_CONTROLLER_DIGITAL_B);
			if ((ispressed && selectedd) || (ispressed && !selectedd && board[(mousey) / 30][(mousex - 120) / 30] != 0)){
				if (!selectedd){
					selectx = (mousex - 120) / 30;
					selecty = (mousey) / 30;
				} else {
					middle = board[selecty][selectx];
					board[selecty][selectx] = 0;
					board[(mousey) / 30][(mousex - 120) / 30] = middle;
				}
				selectedd = !selectedd;
			}

			printf("updated\n");
			if (selectedd){
				drawBoard(selectx, selecty);
			} else {
				drawBoard(-1, -1);
			}
			drawCursor(mousex, mousey);
			
			//end = GetTime() * 1000;
			end = pros::millis();
			if (end - start < fps){
				//WaitTime((fps - (end - start))/1000);
				pros::delay(fps-(end-start));
			}
		}
		lastgame = game;
    }
    return;
}