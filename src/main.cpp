pos paddle1 = {215, 10};
pos paddle2 = {215, 220};
pos ball = {240, 120};
vel ballvel = {0, 0};
float start, end;
float fps = (1000 / (20));
bool gameStarted = false;
bool jumped = false;

float flappyY = 240;
float vel;
float gravity = 0;
int pipeheight1 = -240;
int pipeheight2 = -240;
float pipeX1 = 0;
float pipeX2 = -180;
bool lastnotpressed = true;
int score = 0;

